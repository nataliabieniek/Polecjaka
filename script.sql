create table users
(
    id       serial
        primary key,
    name     varchar(100) not null,
    surname  varchar(100) not null,
    email    varchar(250) not null,
    password varchar(250) not null,
    phone    text
);

alter table users
    owner to dbuser;

create table place
(
    title       varchar(250) not null,
    address     varchar(600) not null,
    district    varchar(50)  not null,
    description text,
    image       varchar(255)
);

alter table place
    owner to dbuser;

